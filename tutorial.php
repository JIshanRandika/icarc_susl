        <div class="workshop-speaker-details" align="justify">
            <h6 style="text-align:center;">About </h6>
           
            Proteomics, the complete set of proteins expressed by an organism, has long been recognized as an important aspect of functional biology. There, many biological processes in living cells are mediated by protein–protein interactions (PPIs). As a result, understanding protein activity and correlating biological roles in the cellular setting necessitates PPI characterisation. To that end, Biopython is an essential programming API for Bioinformaticians to analyse proteomics data because it contains a large number of packages that cover a wide range of data analysis from biological sequence analysis to structural modeling and PPI visualization.           
            
        </div>
        </br>
        <div class="workshop-speaker-details" align="justify">
            <h6 style="text-align:center;">Aims & Objectives </h6>
            <ul class="mb-2" style="margin-left:25px" >
                                    <li>To provide an overview of the biology behind protein synthesis mechanism in the cell </br>
                                    </li>
                                    <li>To provide an overview of Biopython and to explore how it may be used to analyze proteomic data in the context of protein interaction networks </br>
                                    </li>
                                    <li>To demonstrate the use of a PPI network with real-life biological examples </br></li>
                                    </li>
                                 </ul>

           
            
        </div>
        </br>
        <div class="workshop-speaker-details" >
            <h6 style="text-align:center;">Target Audience </h6>
          
            Undergraduate students, graduate students, researchers, academics, and anybody with basic programming skills may benefit from this introductory tutorial</br>
                                 
