        <div class="workshop-speaker-details" align="justify">
            <h6 style="text-align:center;">About </h6>
           
            Software Defined Networks (SDN) is a network management approach that enables the network to be intelligently and centrally controlled or programmed using software applications. SDN mainly focuses on the separation of the network control plane (software) from the data plane (hardware) in networking equipment. It centralizes network intelligence in one network component by disassociating the forwarding process of network packets (data plane) from the routing process (control plane). SDN is widely utilized in cloud network architectures, particularly for dynamically, easily, and quickly updating cloud network configurations.
            
        </div>
        </br>
        <div class="workshop-speaker-details" align="justify">
            <h6 style="text-align:center;">Aims & Objectives </h6>
            <ul class="mb-2" style="margin-left:25px" >
                                    <li>To introduce SDN and why traditional networks must be replaced with SDN in the future</br>
                                    </li>
                                    <li>To examine the SDN architecture </br>
                                    </li>
                                    <li>To describe the different types of controllers available today with different programming languages</br></li>
                                    <li>To discuss how and why cloud service providers are embracing SDN-based architectures
                                    </li>
                                    <li>To provide hands-on experience using Mininet simulation environment to test SDN
                                    </li>
                                 </ul>

           
            
        </div>
        </br>
        <div class="workshop-speaker-details" >
            <h6 style="text-align:center;">Target Audience </h6>
          
           Students and researchers who wish to improve knowledge on SDN concepts</br>
                                 
